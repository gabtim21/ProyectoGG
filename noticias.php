<p><b style="color: #b7b7b7">THE INTERNATIONAL</b></p>
<p><img src="http://cdn.dota2.com/apps/dota2/images/blogfiles/blog_ti_two_pods.jpg" alt="" width="100%" /></p>
<p>Una vez coronado el campeón de Kiev terminando así la temporada de invierno, la atención de la comunidad de Dota se centrará en The International y los debates acerca de qué equipo emergerá del torneo de este año como el mejor equipo indiscutible de Dota del mundo.</p>
<p>Con esto, ya es casi la hora de desvelar el pase de batalla de The International (una vez todos los artículos inmortales estén suficientemente pulidos) y prepararnos para la mayor celebración de Dota anual que atrae a fans, pros y nuevos jugadores al KeyArena de Seattle.</p>
<p>Este año, para diferenciar a los jugadores que apoyen a The International y contribuyan directamente al fondo para premios del torneo, todos los artículos del pase de batalla serán exclusivos hasta el torneo del año siguiente. Los artículos que obtengas se podrán regalar una sola vez y los jugadores que desbloqueen artículos ultra raros podrán también intercambiarlos como en el pasado.</p>
<p>Para aquellos equipos que quieran unirse al plantel del mayor torneo del año, la clasificatoria regional de The International tendrá lugar del 26 al 29 de junio para determinar a los últimos participantes que vendrán a Seattle del 7 al 12 de agosto para intentar obtener la victoria definitiva.</p>
<p><b style="color: #b7b7b7">LA ACTUALIZACIÓN DE HOY</b></p>
<p><img src="http://cdn.dota2.com/apps/dota2/images/blogfiles/blog_hero_stats.jpg" alt="" width="100%" /></p>
<p>La actualización de hoy incluye una sección de estadísticas en la pestaña de información del héroe. Ahora podrás ver fácilmente detalles sobre los atributos de cada héroe y sus estadísticas al subir de nivel, su ataque básico, defensa y movilidad, y también su cantidad inicial de vida y maná y sus regeneraciones.</p>
<p>También hemos actualizado el mecanismo que otorga la habilidad de enviar informes para que dependa de la cantidad de partidas jugadas, hemos arreglado el resumen de conducta del jugador que no mostraba las valoraciones más recientes, y cambiado como funcionan los informes para que los jugadores enemigos solo puedan ser acusados de abusar comunicación.</p>
